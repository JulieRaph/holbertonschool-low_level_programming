#include "3-calc.h"

/**
 * op_add - Function pour additionner des entiers
 * @a: premier int
 * @b: second int
 * Return: always 0 (Success)
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function pour soustraire  des entiers
 * @a: premier int
 * @b: second int
 * Return: always 0 (Success)
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function pour multiplier des entiers
 * @a: premier int
 * @b: second int
 * Return: always 0 (Success)
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function pour diviser des entiers
 * @a: premier int
 * @b: second int
 * Return: always 0 (Success)
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function pour moduler des entiers
 * @a: premier int
 * @b: second int
 * Return: always 0 (Success)
*/

int op_mod(int a, int b)
{
	return (a % b);
}
